function c = jet( n, signed )
%
% c = jet( n=64, signed=false )
%
% Replicate the jet colormap with brighter colors.
%
% In the unsigned case, values go from:
%   dark -> blue -> green -> orange -> red
%
% In the signed case, values go from:
%   negative: dark -> cyan -> blue
%   positive: dark -> yellow -> red
%   
% JH

    if nargin < 1, n = 64; end
    if nargin < 2, signed = false; end
    
    method = 'pchip';

    if signed
        C = [ ...
            -1.00, 0.0, 0.0, 1.0; ...
            -0.80, 0.0, 0.3, 0.9; ...
            -0.35, 0.0, 0.9, 0.9; ...
            -0.00, 0.1, 0.15, 0.1; ...
            +0.35, 0.9, 0.9, 0.0; ...
            +0.80, 0.9, 0.3, 0.0; ...
            +1.00, 1.0, 0.0, 0.0  ...
        ];
        x = linspace(-1,1,n)';
    else
        C = [ ...
            -0.00, 0.0, 0.0, 0.2; ...
            +0.20, 0.1, 0.2, 0.9; ...
            +0.50, 0.1, 0.9, 0.2; ...
            +0.65, 0.8, 0.9, 0.0; ...
            +0.80, 0.9, 0.6, 0.0; ...
            +1.00, 1.0, 0.0, 0.0  ...
        ];
        x = linspace(0,1,n)';
    end

    c = interp1( C(:,1), C(:,2:4), x, method );
    if nargout == 0, dk.cmap.show(c); end

end
