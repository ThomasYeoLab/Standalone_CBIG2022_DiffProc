function c = bgr( n, signed )
%
% c = bgr( n=64, signed=false )
%
% Inspired by the jet colormap, but with darker and more saturated colors.
% 
% In the unsigned case, the values for from blue to red through green.
% In the signed case:
%   - the positive part is the same as the unsigned case;
%   - and the negative part goes from cyan to magenta through yellow with 
%     much brighter colors, giving the impression of a film-negative.
%
% JH

    if nargin < 1, n = 64; end
    if nargin < 2, signed = false; end
    
    method = 'linear';
    if signed
        C = [ ...
            -1.00, 0.9, 0.0, 0.8; ...
            -0.75, 1.0, 0.4, 1.0; ...
            -0.50, 1.0, 1.0, 0.2; ...
            -0.25, 0.5, 1.0, 1.0; ...
            -0.00, 0.0, 0.0, 0.2; ...
            +0.25, 0.1, 0.5, 0.9; ...
            +0.50, 0.5, 0.9, 0.1; ...
            +0.75, 0.9, 0.5, 0.1; ...
            +1.00, 0.9, 0.0, 0.1  ...
        ];
        x = linspace(-1,1,n)';
    else
        C = [ ...
            -0.00, 0.0, 0.0, 0.2; ...
            +0.25, 0.1, 0.5, 0.9; ...
            +0.50, 0.5, 0.9, 0.1; ...
            +0.75, 0.9, 0.5, 0.1; ...
            +1.00, 0.9, 0.0, 0.1  ...
        ];
        x = linspace(0,1,n)';
    end

    c = interp1( C(:,1), C(:,2:4), x, method );
    if nargout == 0, dk.cmap.show(c); end

end
